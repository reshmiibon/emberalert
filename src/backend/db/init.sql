-- DataPurger procedure 
DROP PROCEDURE IF EXISTS purge_data;
DELIMITER $$
$$
CREATE PROCEDURE 
	purge_data(
		ttl INT
	)
	SQL SECURITY INVOKER
BEGIN
	-- Get the latest engine run that is at least 7 days old
	DECLARE latest_to_purge_id INT;
	
	SELECT 
		MAX(er.id) 
	INTO 
		latest_to_purge_id 
	FROM 
		engine_run er 
	WHERE 
		DATEDIFF(NOW(), er.generation_date) >= ttl;
		
	-- Purge point data
	DELETE 
		pp 
	FROM 
		polygon_point pp 
		INNER JOIN mask m 
	WHERE 
		m.run_id <= latest_to_purge_id;
	
	-- Purge region data
	DELETE 
		r 
	FROM 
		region r 
	WHERE 
		r.run_id <= latest_to_purge_id;
	
	-- Delete mask data
	DELETE 
		m
	FROM 
		mask m 
	WHERE 
		m.run_id <= latest_to_purge_id;
	
	DELETE 
		f 
	FROM 
		fire f 
	WHERE 
		DATEDIFF(NOW(), f.identification_date) >= ttl;
	
	-- Update engine run table
	UPDATE 
		engine_run er 
	SET 
		er.purge_date = NOW()
	WHERE 
		er.id <= latest_to_purge_id;
END
$$
DELIMITER ;



-- remove last failed run procedure 
DROP PROCEDURE IF EXISTS remove_failed_run;
DELIMITER $$
$$
CREATE PROCEDURE 
	remove_failed_run()
	SQL SECURITY INVOKER
BEGIN
    DECLARE run_id INT;
	DECLARE gen_date DATETIME;

	SELECT 
		MAX(id) 
	INTO 
		run_id 
	FROM 
		engine_run;

	SELECT 
		generation_date 
	INTO 
		gen_date 
	FROM 
		engine_run 
	WHERE 
		id = run_id;
	
	DELETE 
		pp 
	FROM 
		polygon_point pp 
		INNER JOIN mask m
	WHERE 
		m.run_id = run_id;
	
	DELETE 
		r 
	FROM 
		region r
	WHERE 
		r.run_id = run_id;
	
	DELETE 
		m 
	FROM 
		mask m 
	WHERE  
		m.run_id = run_id;
	
	DELETE 
		f 
	FROM 
		fire f
	WHERE 
		ABS(HOUR(TIMEDIFF(f.identification_date, gen_date))) < 2;
	
	UPDATE 
		engine_run 
	SET 
		purge_date = NOW() 
	WHERE 
		id = run_id;
END
$$
DELIMITER ;



