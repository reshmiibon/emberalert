     SELECT 
          MAX(er.generation_date) 
     INTO 
          latest_run_date 
     FROM 
          engine_run er 
     WHERE 
          er.purge_date IS NULL;
          
     -- Set the is_active to 0
     UPDATE 
          fire f 
     SET 
          f.is_active = 0 
     WHERE 
          ABS(HOUR(TIMEDIFF(f.identification_date, latest_run_date))) > 2;
END 
$$
